  const SUPPORTED = Object.keys(STRINGS);

  function detect() {
    const stored = localStorage.getItem('lang');
    if (stored && SUPPORTED.includes(stored)) return stored;
    const nav = (navigator.language || 'en').toLowerCase().split('-')[0];
    return SUPPORTED.includes(nav) ? nav : 'en';
  }

  const LOCALE_MAP = { en: 'en-US', ko: 'ko-KR', ja: 'ja-JP', zh: 'zh-CN', ru: 'ru-RU' };

  function formatDate(iso, lang) {
    const [y, m, d] = iso.split('-').map(Number);
    const date = new Date(Date.UTC(y, m - 1, d));
    try {
      return new Intl.DateTimeFormat(LOCALE_MAP[lang] || 'en-US', {
        year: 'numeric', month: 'long', day: 'numeric', timeZone: 'UTC'
      }).format(date);
    } catch {
      return iso;
    }
  }

  function apply(lang) {
    const dict = STRINGS[lang] || STRINGS.en;
    document.documentElement.lang = lang;
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const val = dict[key] || STRINGS.en[key];
      if (val) el.textContent = val;
    });
    document.querySelectorAll('[data-date]').forEach(el => {
      el.textContent = formatDate(el.getAttribute('data-date'), lang);
    });
    const nowYear = document.getElementById('tl-now-year');
    if (nowYear) nowYear.textContent = String(new Date().getUTCFullYear());
    document.querySelectorAll('.lang-switch button').forEach(b => {
      b.setAttribute('aria-current', b.dataset.lang === lang ? 'true' : 'false');
    });
    window.I18N = { lang, t: k => dict[k] || STRINGS.en[k] || k };
    window.dispatchEvent(new CustomEvent('i18n:changed', { detail: { lang } }));
  }

  document.querySelectorAll('.lang-switch button').forEach(btn => {
    btn.addEventListener('click', () => {
      const lang = btn.dataset.lang;
      localStorage.setItem('lang', lang);
      apply(lang);
    });
  });

  apply(detect());
